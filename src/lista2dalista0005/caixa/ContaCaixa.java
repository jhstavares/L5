package lista2dalista0005.caixa;

public class ContaCaixa {
    double saldo;
    double credito;

    double debito;

    public ContaCaixa() {
        this.saldo = 1000.00;
        this.credito = credito;
        this.debito = debito;
    }
    public double credito () {
        return saldo += credito;
    }
    public double debito(){
        return saldo -= debito;

    }
}

